package org.alumnievent.model;

public class OragnizerModel {
	private int OrganizerId;
	private String OrganizerName;
	private String OrganizerEmail;
	private String OrganizerContact;
	private String OrganizerPassword;
	private String OrganizerAddress;
	public int getOrganizerId() {
		return OrganizerId;
	}
	public void setOrganizerId(int organizerId) {
		OrganizerId = organizerId;
	}
	public String getOrganizerName() {
		return OrganizerName;
	}
	public void setOrganizerName(String organizerName) {
		OrganizerName = organizerName;
	}
	public String getOrganizerEmail() {
		return OrganizerEmail;
	}
	public void setOrganizerEmail(String organizerEmail) {
		OrganizerEmail = organizerEmail;
	}
	public String getOrganizerContact() {
		return OrganizerContact;
	}
	public void setOrganizerContact(String organizerContact) {
		OrganizerContact = organizerContact;
	}
	public String getOrganizerPassword() {
		return OrganizerPassword;
	}
	public void setOrganizerPassword(String organizerPassword) {
		OrganizerPassword = organizerPassword;
	}
	public String getOrganizerAddress() {
		return OrganizerAddress;
	}
	public void setOrganizerAddress(String organizerAddress) {
		OrganizerAddress = organizerAddress;
	}
	
	
}
